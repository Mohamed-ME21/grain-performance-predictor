    axes[1].grid(True, alpha=0.3)
    plt.suptitle(f'{label} — Input Curves', fontsize=13, fontweight='bold')
    plt.tight_layout()
    plt.savefig(f'{label.replace(" ","_")}_prediction.png', dpi=150, bbox_inches='tight')
    plt.show()

    return result


