def weighted_mse(y_true, y_pred):

    weights = tf.constant(
        [1.5, 2.0, 1.5, 1.5, 4.0, 4.0],
        dtype=tf.float32
    )

    error = tf.square(y_true - y_pred)

    weighted_error = error * weights

    return tf.reduce_mean(weighted_error)

