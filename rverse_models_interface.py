    print(f"  Total Impulse   : {total_impulse:.2f} Ns")
    print(f"  Burn Time       : {burn_time:.4f} s")
    print(f"  Max Thrust      : {max_thrust:.2f} N")
    print("="*50)

    return dict(zip(DIM_NAMES, pred_dims))


